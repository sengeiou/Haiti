package com.aimir.service.mvm.bean;


public class VEEParameterData {
	private Boolean chked;
	private String localName;
	private String item;
	private String useThreshold;
	private String condition;
	public Boolean getChked() {
		return chked;
	}
	public void setChked(Boolean chked) {
		this.chked = chked;
	}
	public String getLocalName() {
		return localName;
	}
	public void setLocalName(String localName) {
		this.localName = localName;
	}
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public String getUseThreshold() {
		return useThreshold;
	}
	public void setUseThreshold(String useThreshold) {
		this.useThreshold = useThreshold;
	}
	public String getCondition() {
		return condition;
	}
	public void setCondition(String condition) {
		this.condition = condition;
	}
	
	
}
